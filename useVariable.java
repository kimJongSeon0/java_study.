
public class useVariable {

	public static void main(String[] args) {

		int num1;
		num1 = 10;

		int num2 = 20;      //int (자료형)    num1(변수명)  두개 합쳐서 변수선언
		int num3 = num1 + num2;

		System.out.println(num1 + "+" + num2 + " = " + num3);
	}
}



